// ─── CURVA SOLAR REALISTA PARA CÓRDOBA (Otoño, ~5.5 kWh/m²/día) ───
export function generateSolarCurve(installation: Installation): { time: string; generation: number }[] {
  const peak = installation.powerKwp * 0.75; // 75% de eficiencia pico
  const hours = [
    { hour: 6, factor: 0.02 },
    { hour: 7, factor: 0.08 },
    { hour: 8, factor: 0.20 },
    { hour: 9, factor: 0.38 },
    { hour: 10, factor: 0.58 },
    { hour: 11, factor: 0.78 },
    { hour: 12, factor: 0.92 },
    { hour: 13, factor: 1.0 },
    { hour: 14, factor: 0.95 },
    { hour: 15, factor: 0.82 },
    { hour: 16, factor: 0.62 },
    { hour: 17, factor: 0.40 },
    { hour: 18, factor: 0.18 },
    { hour: 19, factor: 0.05 },
  ];

  if (installation.status === "mantenimiento") {
    return hours.map((h) => ({
      time: `${h.hour}:00`,
      generation: 0,
    }));
  }

  const efficiency = installation.status === "alerta" ? 0.4 : 1.0;

  return hours.map((h) => ({
    time: `${h.hour}:00`,
    generation: Math.round(peak * h.factor * efficiency * (0.95 + Math.random() * 0.1)),
  }));
}

// ─── GENERACIÓN MENSUAL (últimos 6 meses) ───
export function generateMonthlyData(installation: Installation): { month: string; generation: number }[] {
  const months = ["Nov", "Dic", "Ene", "Feb", "Mar", "Abr"];
  const baseMonthly = installation.powerKwp * 135; // ~135 kWh/kWp/mes en Córdoba
  const seasonalFactors = [1.1, 1.15, 1.2, 1.1, 0.95, 0.85]; // Otoño: bajando

  return months.map((month, i) => ({
    month,
    generation: Math.round(baseMonthly * seasonalFactors[i] * (0.92 + Math.random() * 0.08)),
  }));
}

